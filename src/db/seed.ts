export const seedDatabase = async () => {
  try {
    console.log('Seeding database...');
    // await db.delete(workouts);

    const parsedEvents = parseIcsPlan(TEST_ICS_DATA);
    const formattedWorkouts = parsedEvents.workouts.map((e) => ({
      date: e.date.toISOString(), // Ensure ISO string
      title: e.title,
      description: e.description || '',
      distanceKm: e.distanceKm, // Simple fallback or helper
      type: e.type, // Helper to set 'Easy' | 'Tempo' etc
      externalId: e.stravaActivityId,
      isCompleted: false,
    }));

    await db.insert(workouts).values(formattedWorkouts);

    console.log(`Seeded ${formattedWorkouts.length} workouts`);
    return { success: true };
  } catch (e) {
    console.error('Seeding failed:', e);
    return { success: false, error: e };
  }
};
